#include <header.h>

void	ft_putnbr(int nb)
{
	int d;

	if (nb == 0)
		ft_putchar('0');
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	d = 1000000000;
	while (d > nb)
		d /= 10;
	while (d > 0)
	{
		ft_putchar((nb / d) + 48);
		nb = nb % d;
		d /= 10;
	}
}
